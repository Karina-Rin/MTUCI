# Функции принадлежности по входным переменным.
import matplotlib.pyplot as plt

# Заданные параметры
объем_воды = [50, 100, 150, 200]
температура_воды = [10, 20, 45, 98]
объем_кофе = [5, 10, 15, 20]
время_варки = [30, 60, 120, 180]

# Функции принадлежности
крепость_кофе = ["слабый", "средний", "крепкий", "слишком крепкий"]

# Построение графиков принадлежности
plt.figure(figsize=(10, 6))

# Объем воды
plt.subplot(2, 2, 1)
plt.plot(объем_воды, [1, 0, 0, 0], label="очень мало")
plt.plot(объем_воды, [0, 1, 0, 0], label="мало")
plt.plot(объем_воды, [0, 0, 1, 0], label="средний")
plt.plot(объем_воды, [0, 0, 0, 1], label="много")
plt.xlabel("Объем воды (мл)")
plt.ylabel("Принадлежность")
plt.legend()

# Температура воды
plt.subplot(2, 2, 2)
plt.plot(температура_воды, [1, 0, 0, 0], label="холодная")
plt.plot(температура_воды, [0, 1, 0, 0], label="теплая")
plt.plot(температура_воды, [0, 0, 1, 0], label="горячая")
plt.plot(температура_воды, [0, 0, 0, 1], label="кипяток")
plt.xlabel("Температура воды (°C)")
plt.ylabel("Принадлежность")
plt.legend()

# Объем кофе
plt.subplot(2, 2, 3)
plt.plot(объем_кофе, [1, 0, 0, 0], label="мало")
plt.plot(объем_кофе, [0, 1, 0, 0], label="средний")
plt.plot(объем_кофе, [0, 0, 1, 0], label="много")
plt.plot(объем_кофе, [0, 0, 0, 1], label="очень много")
plt.xlabel("Объем кофе (г)")
plt.ylabel("Принадлежность")
plt.legend()

# Время варки
plt.subplot(2, 2, 4)
plt.plot(время_варки, [1, 0, 0, 0], label="мало")
plt.plot(время_варки, [0, 1, 0, 0], label="среднее")
plt.plot(время_варки, [0, 0, 1, 0], label="много")
plt.plot(время_варки, [0, 0, 0, 1], label="очень много")
plt.xlabel("Время варки (сек)")
plt.ylabel("Принадлежность")
plt.legend()

plt.tight_layout()
plt.show()
